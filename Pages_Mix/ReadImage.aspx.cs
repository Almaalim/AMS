using System;
using System.Collections.Generic;
using System.Linq;
using System.Web;
using System.Web.UI;
using System.Web.UI.WebControls;
using System.Data;
using System.Data.SqlClient;

public partial class ReadImage : System.Web.UI.Page
{
    /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    DataTable DT;
    DBFun DBCs = new DBFun();
    /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    protected void Page_Load(object sender, EventArgs e)
    {
        try
        {
            string ID = (Request.QueryString["ID"] != null) ? Request.QueryString["ID"] : "";

            if (ID == "Logo") 
            {  
                DT = DBCs.FetchData(new SqlCommand(" SELECT AppLogo FROM ApplicationSetup "));
            }
            
            if (DBCs.IsNullOrEmpty(DT))        { return; }
            if (DT.Rows[0][0] == DBNull.Value) { return; }

            Response.BinaryWrite((Byte[])DT.Rows[0][0]);
            Response.End();
        }
        catch (Exception e1) { }
    }
    /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
}